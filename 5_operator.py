# 연산자
print(1+1)  # 2
print(3-2)  # 1
print(5*2)  # 10
print(6/3)  # 2

print(2**3)  # 2^3 = 8
print(5 % 3)  # 5/3 의 나머지 = 2
print(10 % 3)  # 1
print(5//3)  # 5/3의 몫 = 1
print(10//3)  # 3

print(10 > 3)  # True
print(4 >= 7)  # Flase
print(10 < 3)  # Flase
print(5 <= 5)  # True

print(3 == 3)  # 같다 True
print(4 == 2)  # Flase
print(3+4 == 7)  # True

print(1 != 3)  # 같지않다 True
print(not (1 != 3))  # 같지 않지 않다 Flase

print((3 > 0) and (3 < 5))  # and 는 둘다 T 여야 T
print((3 > 0) & (3 < 5))  # and = &

print((3 > 0) or (3 > 5))  # or는 둘중하나 T 면 T
print((3 > 0) | (3 > 5))  # or = |

print(5 > 4 > 3)  # Ture
print(5 > 4 > 7)  # Flase
