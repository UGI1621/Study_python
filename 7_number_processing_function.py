# 숫자 처리 함수

from math import *  # math 라이브러리 갖고옴
print(abs(-5))  # -5의 절댓값 = 5
print(pow(4, 2))  # 4^2 = 16
print(max(5, 12))  # 5와 12중 더큰값 = 12
print(min(5, 11))  # 5와 11중 더 작은값 = 5
print(round(3.14))  # 3.14를 반올림 = 3
print(round(4.99))  # 4

# 여기서부터 math 라이브러리 씀
print(floor(4.99))  # 내림 = 4
print(ceil(3.14))  # 올림 = 4
print(sqrt(16))  # 제곱근 = 4
